#!/usr/bin/env python3
print("Enter two integers")
x = int(input("Enter x:"))
y = int(input("Enter y:"))

# Arithmetic operators
print("\nArithmetic Operators")
print('x + y =', x + y)
print('x - y =', x - y)
print('x * y =', x * y)
print('x / y =', x / y)
print('x // y =', x // y)
print('x ** y =', x ** y)

# Comparison operators
print("\nComparison operators")
print('x > y  is', x > y)
print('x < y  is', x < y)
print('x == y is', x == y)
print('x != y is', x != y)
print('x >= y is', x >= y)
print('x <= y is', x <= y)